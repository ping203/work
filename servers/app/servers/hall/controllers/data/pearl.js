/**
 * 添加钻石记录
 */
async function _add_pearl_log(data) {
    return new Promise(function (resolve, reject) {
        myDao.addPearlLog(data, function(err, result) {
            if (err) {
                logger.error('更新玩家钻石数据失败 err:', err);
                reject(err);
            }
            resolve(logicResponse.ask(result));
        });
    });
}


