/**
 * 添加商城记录
 */
function add_shop_log(req, res) {
    data_util.request_info(req, "add_shop_log");
    var dataObj = {};
    if (!(dataObj = data_util.get_dao_data(req, res))) {
        return;
    }
    BuzzUtil.cacheLinkDataApi(dataObj, "add_shop_log");
    
    myDao.addShopLog(dataObj, function (err, rows) {
        if (err) {
            res.success({ type: 1, msg: '更新玩家商城数据失败', err: err });
        } else {
            // 需要返回用户数据进行更新
            res.success({ type: 1, msg: '更新玩家商城购买数据成功', data: rows[0] });
        }
    });
}


