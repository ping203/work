/**
 * 抽取奖励(可抽取一次，也可以抽取十次)
 */
function getDraw(req, res) {
    var aes = req.body.aes;
    var dataObj = {};
    if (!(dataObj = data_util.get_dao_data(req, res))) {
        return;
    }
    if (DEBUG) console.log('dataObj: ', dataObj);
    
    myDao.getDraw(dataObj, function (err, account) {
        if (err) {
            res.success({ type: 1, msg: '抽奖失败', err: err });
        } else {
            var res_data = buzz_cst_game.getResData(account.toJSON(), aes);
            res.success({ type: 1, msg: '抽奖成功', data: res_data, aes: aes });
        }
    });
}


